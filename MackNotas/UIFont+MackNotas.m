#import "UIFont+MackNotas.h"

@implementation UIFont (MackNotas)

+ (UIFont *)bitterBoldWithSize:(CGFloat)size {

    return [UIFont fontWithName:@"Bitter-Bold" size:size];
}

+ (UIFont *)bitterRegularWithSize:(CGFloat)size {

    return [UIFont fontWithName:@"Bitter-Regular" size:size];
}

@end
